public class day1 {
    public static void main(String[] args) {
        // Examples of primitive data types in Java
        int age = 30; // integer
        System.out.println(age);
        double salary = 2500.75; // floating-point number
        System.out.println(salary);
        char gender = 'M'; // character
        System.out.println(gender);
        boolean isActive = true; // boolean (true/false)
        System.out.println(isActive);

        // Example of reference data type (object) in Java
        String name = "John Doe"; // String object
        System.out.println(name);

        // Examples of variables in Java
        int numStudents = 50; // integer variable
        System.out.println(numStudents);
        double averageGrade = 85.5; // double (floating-point) variable
        System.out.println(averageGrade);
        String department = "Computer Science"; // String variable
        System.out.println(department);
        boolean isGraduated = true; // boolean variable
        System.out.println(isGraduated);

    }
}

